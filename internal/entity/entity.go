// Пакет entity определяет базовые интерфейсы для всех игровых сущностей.
package entity

import (
	"TheFiaskoTest/internal/render"

	"TheFiaskoTest/internal/common"

	"github.com/hajimehoshi/ebiten/v2"
)

// Entity — базовый интерфейс для всех объектов в игре (игрок, враги, предметы и т.д.).
// Любая сущность должна уметь обновлять своё состояние и отрисовываться на экране.
type Entity interface {
	// Update вызывается каждый кадр для обновления логики сущности.
	// ctx предоставляет параметры мира (скорость, смещение), которые могут влиять на поведение.
	Update(ctx common.WorldContext)

	// Draw отвечает за отрисовку сущности на экране.
	// screen — поверхность, на которую нужно рисовать.
	// cam — камера, преобразующая мировые координаты в экранные.
	// ctx — контекст мира (может содержать дополнительную информацию для отрисовки).
	Draw(screen *ebiten.Image, cam *render.Camera, ctx common.WorldContext)

	// GetZ возвращает Z-координату сущности (глубину/слой).
	// Используется для сортировки при отрисовке: чем меньше Z, тем раньше рисуется сущность (дальше от камеры).
	GetZ() float64

	// SetZ устанавливает Z-координату сущности.
	SetZ(z float64)
}
